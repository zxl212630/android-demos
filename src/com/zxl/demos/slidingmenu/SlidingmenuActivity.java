package com.zxl.demos.slidingmenu;

import com.jeremyfeinstein.slidingmenu.lib.SlidingMenu;
import com.zxl.demos.R;

import android.os.Bundle;
import android.support.v7.app.ActionBarActivity;

public class SlidingmenuActivity extends ActionBarActivity{
	private SlidingMenu mSlidingMenu;

	@Override
	protected void onCreate(Bundle savedInstanceState) {
		super.onCreate(savedInstanceState);
		
		setContentView(R.layout.activity_slidingmenu);
		
		mSlidingMenu = new SlidingMenu(this);
		mSlidingMenu.setMode(SlidingMenu.LEFT);
		mSlidingMenu.setTouchModeAbove(SlidingMenu.TOUCHMODE_MARGIN);
		mSlidingMenu.setShadowWidthRes(R.dimen.slidingmenu_shadow_width);
		mSlidingMenu.setShadowDrawable(R.drawable.slidingmenu_shadow);
		mSlidingMenu.setBehindOffsetRes(R.dimen.slidingmenu_offset);
		mSlidingMenu.setFadeDegree(0.35f);
		mSlidingMenu.attachToActivity(this, SlidingMenu.SLIDING_CONTENT);
		mSlidingMenu.setMenu(R.layout.slidingmenu_menu);
	}

	@Override
	public void onBackPressed() {
		if(mSlidingMenu.isMenuShowing()){
			mSlidingMenu.showContent();
		}else{
			super.onBackPressed();
		}
	}
	
	
}
